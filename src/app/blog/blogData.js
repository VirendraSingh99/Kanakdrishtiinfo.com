// Auto-generate slug
blogs.forEach((blog) => {
  blog.slug = titleToSlug(blog.title);
});

export function getBlogBySlug(slug) {
  return blogs.find((b) => b.slug === slug) || null;
}

export function getRelatedBlogs(currentSlug, limit = 3) {
  const current = getBlogBySlug(currentSlug);
  if (!current) return [];
  return blogs
    .filter((b) => b.slug !== currentSlug && b.category === current.category)
    .slice(0, limit);
}

export function getRecentBlogs(limit = 4) {
  return [...blogs].sort((a, b) => b.id - a.id).slice(0, limit);
}

export function getAllSlugs() {
  return blogs.map((blog) => ({ slug: blog.slug }));
}
